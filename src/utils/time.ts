export const isFriday = (date: Date) =>
  date.getFullYear() === 2026 && date.getMonth() === 2 && date.getDate() === 20;
export const isSaturday = (date: Date) =>
  date.getFullYear() === 2026 && date.getMonth() === 2 && date.getDate() === 21;
export const isSunday = (date: Date) =>
  date.getFullYear() === 2026 && date.getMonth() === 2 && date.getDate() === 22;
